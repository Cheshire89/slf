     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $model = new Property();
        $results = Rets::fetchUpdates();
        foreach($results->toArray() as $property){
            $subset = array_unique(Arr::only($property, $model->getFillable()));
            Property::updateOrCreate($subset);
        }
    }

